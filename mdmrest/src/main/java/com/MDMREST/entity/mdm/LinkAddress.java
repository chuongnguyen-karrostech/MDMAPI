package com.MDMREST.entity.mdm;

public class LinkAddress{
	private String url;
	private Object body;
	public LinkAddress() {
		super();
	}
	public String geturl() {
		return url;
	}

	public void seturl(String url) {
		this.url = url;
	}
	
	public Object getbody() {
		return body;
	}

	public void setbody(Object body) {
		this.body = body;
	}
	
}